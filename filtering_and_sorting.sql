-- ============================================================
-- Hospital Management Database
-- Filtering & Sorting Queries
-- ============================================================

-- ============================================================
-- Q16. Display patients between 20 and 40 years old.
-- ============================================================

SELECT *
FROM Patients
WHERE age BETWEEN 20 AND 40;

---------------------------------------------------------------

-- ============================================================
-- Q17. Display patients from Delhi.
-- ============================================================

SELECT *
FROM Patients
WHERE city = 'Delhi';

---------------------------------------------------------------

-- ============================================================
-- Q18. Display patients whose blood group is O+.
-- ============================================================

SELECT *
FROM Patients
WHERE blood_group = 'O+';

---------------------------------------------------------------

-- ============================================================
-- Q19. Display all doctors with more than 10 years of experience.
-- ============================================================

SELECT *
FROM Doctors
WHERE experience_years > 10;

---------------------------------------------------------------

-- ============================================================
-- Q20. Display medicines costing more than ₹100.
-- ============================================================

SELECT *
FROM Medicines
WHERE unit_price > 100;

---------------------------------------------------------------

-- ============================================================
-- Q21. Display bills greater than ₹1500.
-- ============================================================

SELECT *
FROM Bills
WHERE total_amount > 1500;

---------------------------------------------------------------

-- ============================================================
-- Q22. Display appointments that are scheduled.
-- ============================================================

SELECT *
FROM Appointments
WHERE status = 'Scheduled';

---------------------------------------------------------------

-- ============================================================
-- Q23. Display appointments that are completed.
-- ============================================================

SELECT *
FROM Appointments
WHERE status = 'Completed';

---------------------------------------------------------------

-- ============================================================
-- Q24. Display doctors ordered by highest salary.
-- ============================================================

SELECT *
FROM Doctors
ORDER BY salary DESC;

---------------------------------------------------------------

-- ============================================================
-- Q25. Display patients ordered by age (oldest first).
-- ============================================================

SELECT *
FROM Patients
ORDER BY age DESC;

---------------------------------------------------------------

-- ============================================================
-- Q26. Display the 5 most expensive medicines.
-- ============================================================

SELECT *
FROM Medicines
ORDER BY unit_price DESC
LIMIT 5;

---------------------------------------------------------------

-- ============================================================
-- Q27. Display the 10 youngest patients.
-- ============================================================

SELECT *
FROM Patients
ORDER BY age ASC
LIMIT 10;

---------------------------------------------------------------

-- ============================================================
-- Q28. Display distinct patient cities.
-- ============================================================

SELECT DISTINCT city
FROM Patients
ORDER BY city;

---------------------------------------------------------------

-- ============================================================
-- Q29. Display doctors whose name starts with 'Dr. A'.
-- ============================================================

SELECT *
FROM Doctors
WHERE doctor_name LIKE 'Dr. A%';

---------------------------------------------------------------

-- ============================================================
-- Q30. Display medicines manufactured by Cipla.
-- ============================================================

SELECT *
FROM Medicines
WHERE manufacturer = 'Cipla';

---------------------------------------------------------------

-- ============================================================
-- Q31. Display patients with blood group A+ or B+.
-- ============================================================

SELECT *
FROM Patients
WHERE blood_group IN ('A+','B+');

---------------------------------------------------------------

-- ============================================================
-- Q32. Display patients admitted after 1 June 2026.
-- ============================================================

SELECT *
FROM Patients
WHERE admission_date > '2026-06-01';

---------------------------------------------------------------

-- ============================================================
-- Q33. Display doctors earning between ₹150000 and ₹180000.
-- ============================================================

SELECT *
FROM Doctors
WHERE salary BETWEEN 150000 AND 180000;

---------------------------------------------------------------

-- ============================================================
-- Q34. Display bills that were paid using UPI.
-- ============================================================

SELECT *
FROM Bills
WHERE payment_method = 'UPI';

---------------------------------------------------------------

-- ============================================================
-- Q35. Display appointments sorted by appointment date.
-- ============================================================

SELECT *
FROM Appointments
ORDER BY appointment_date;