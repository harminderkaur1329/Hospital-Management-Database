-- ============================================================
-- Hospital Management Database
-- Basic SQL Queries
-- ============================================================

-- ============================================================
-- Q1. Display all patients.
-- ============================================================

SELECT * FROM Patients;

---------------------------------------------------------------

-- ============================================================
-- Q2. Display all doctors.
-- ============================================================

SELECT * FROM Doctors;

---------------------------------------------------------------

-- ============================================================
-- Q3. Display all departments.
-- ============================================================

SELECT * FROM Departments;

---------------------------------------------------------------

-- ============================================================
-- Q4. Display all medicines.
-- ============================================================

SELECT * FROM Medicines;

---------------------------------------------------------------

-- ============================================================
-- Q5. Display all appointments.
-- ============================================================

SELECT * FROM Appointments;

---------------------------------------------------------------

-- ============================================================
-- Q6. Display all bills.
-- ============================================================

SELECT * FROM Bills;

---------------------------------------------------------------

-- ============================================================
-- Q7. Display all prescriptions.
-- ============================================================

SELECT * FROM Prescriptions;

---------------------------------------------------------------

-- ============================================================
-- Q8. Display all female patients.
-- ============================================================

SELECT *
FROM Patients
WHERE gender = 'Female';

---------------------------------------------------------------

-- ============================================================
-- Q9. Display all male patients.
-- ============================================================

SELECT *
FROM Patients
WHERE gender = 'Male';

---------------------------------------------------------------

-- ============================================================
-- Q10. Display all doctors working in Cardiology.
-- ============================================================

SELECT *
FROM Doctors
WHERE specialization = 'Cardiologist';

---------------------------------------------------------------

-- ============================================================
-- Q11. Display patients above 60 years.
-- ============================================================

SELECT *
FROM Patients
WHERE age > 60;

---------------------------------------------------------------

-- ============================================================
-- Q12. Display children below 15 years.
-- ============================================================

SELECT *
FROM Patients
WHERE age < 15;

---------------------------------------------------------------

-- ============================================================
-- Q13. Display appointments scheduled for today.
-- ============================================================

SELECT *
FROM Appointments
WHERE appointment_date = CURRENT_DATE;

---------------------------------------------------------------

-- ============================================================
-- Q14. Display cancelled appointments.
-- ============================================================

SELECT *
FROM Appointments
WHERE status = 'Cancelled';

---------------------------------------------------------------

-- ============================================================
-- Q15. Display pending bill payments.
-- ============================================================

SELECT *
FROM Bills
WHERE payment_status = 'Pending';